splitstring = "This line will\nget split\nover multiple lines"
print(splitstring)

tabbedstring = "1\t2\t3\t4\t5"
print(tabbedstring)

#single quote with escape character
print('The pet owner said "No, no, \'e\'s uh,... he\'s resting".')

#double quote with escape character
print("The pet owner said \"No, no, 'e's uh,... he's resting\".")

#triple quote without escape character
print("""The pet owner said "No, no, 'e's uh,... he's resting".""")



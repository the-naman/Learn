a=12; b=3
print("a=",a, " ","b=",b)

#sum
print("sum= ", a+b)

#sub
print("sub= ", a-b)

#mul
print("mul= ", a*b)

#div : always give output in float by default
print("div= ", a/b)

#int division or double division
print("intdiv= ", a//b)

#mod or rem
print("rem= ", a%b)


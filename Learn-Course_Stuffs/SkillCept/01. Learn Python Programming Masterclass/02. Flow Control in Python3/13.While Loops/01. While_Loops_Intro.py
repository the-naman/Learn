# Wile Loop:
#     1. while loop we can execute a set of statements as long as a condition is true.(Don't need to know how many times.)
#     2. Note: i must be initialized and, remember to increment i, or else the loop will continue forever.
#     3. We can also use else with while same as is-else.



i = 0
while i < 10:
    print("Now i is {}".format(i))
    i = i+1
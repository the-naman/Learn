# Augmented Assignement :
#     1. Short Hand for long form. 
#     2. Augmented Assignment operators are used to assign values to same variables.


#         Operator 	Example 	Same As 	
#             += 	    x += 3 	    x = x + 3 	
#             -= 	    x -= 3 	    x = x - 3 	
#             *= 	    x *= 3 	    x = x * 3 	
#             /= 	    x /= 3 	    x = x / 3 	
#             %= 	    x %= 3 	    x = x % 3 	
#             //= 	    x //= 3 	x = x // 3 	
#             **= 	    x **= 3 	x = x ** 3 	
#             &= 	    x &= 3 	    x = x & 3 	
#             |= 	    x |= 3 	    x = x | 3 	
#             ^= 	    x ^= 3 	    x = x ^ 3 	
#             >>= 	    x >>= 3 	x = x >> 3 	
#             <<= 	    x <<= 3 	x = x << 3

x=0
x += 3 
print("x= ", x)    

y=1
y = y + 3
print("y= ", y)    

z = "Good "
z += "Morning "
print(z)

